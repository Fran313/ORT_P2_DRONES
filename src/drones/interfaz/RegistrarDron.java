    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if(txtIdentificacion.getText().equals("") ||
           txtModelo.getText().equals("") ||
           !Pattern.matches("[0-9]+", txtTipo.getText()) ||
           txtTipo.getText().equals("")) {
        
        JOptionPane.showMessageDialog(null, "Campos Incorrectos",
                "Error", JOptionPane.ERROR_MESSAGE);        
        } else {
            this.sistema.agregarDron(txtIdentificacion.getText(),
                    txtModelo.getText(),
                    Integer.parseInt(txtTipo.getText()));                        
            
            hydrate();            
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    public void hydrate () {
        ArrayList<Dron> drones = this.sistema.getDrones();
        
        DefaultTableModel tblModel = (DefaultTableModel) tblDrones.getModel();
        
        tblModel.setRowCount(0);
        
        for (int i = 0; i < drones.size(); i++) {            
            String[] row = {drones.get(i).getIdentificacion(),
                            drones.get(i).getModelo(),
                            String.valueOf(drones.get(i).getCamara())};
            
            tblModel.addRow(row);            
        }
        
        txtIdentificacion.setText("");
        txtModelo.setText("");
        txtTipo.setText("");
        
    }

